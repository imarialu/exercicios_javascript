/* Declare duas variáveis e atribua valores booleanos a elas.
   Use os operadores lógicos AND, OR e NOT para realizar operações lógicas entre as variáveis.
   Imprima os resultados.
*/

let valor1 = true;
let valor2 = false;

console.log("AND: ", valor1 && valor2);
console.log("OR: ", valor1 || valor2);
console.log("NOT: ", valor1 != valor2);